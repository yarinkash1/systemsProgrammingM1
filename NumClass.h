


/* will return if a number is Armstrong number */
int isArmstrong(int);

/* will return if a number is a palindrome */
int isPalindrome(int);

/* will return if a number is prime*/
int isPrime(int);

/* Strong number is a special number whose sum of the factorial of digits is equal to the original
number. */
int isStrong(int);

/*Helper functions for convenience*/

/*Given a number X, will calculate X! (X factorial)*/
int factorial(int);

/*Will return the number of digits a number has*/
int numLength(int);

/* Given numbers X and Y, will calculate X^Y (X to the power of Y) */
int power(int,int);

/* A helper function to the original isArmStrong function in the recursive class to check if the number is armstrong*/
int isArmstrongHelper(int,int,int);


